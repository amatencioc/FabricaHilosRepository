SELECT C.COD_CLIENTE,
       X.NOMBRE,
       T.ABREVIADA VENDEDOR,
       TO_CHAR(C.FECHA,'YYYY/MM') ANIO,
	   X.RUC,
       SUM(B.CANTIDAD * E.FACTOR) IMP_NETO
  FROM ITEMDOCU     B,
       DOCUVENT     C,
       ARTICUL      A,
	   CLIENTES     X,
	   TABLAS_AUXILIARES T,
	   EQUIVALENCIA E
 WHERE C.TIPODOC = B.TIPODOC
   AND C.SERIE = B.SERIE
   AND C.NUMERO = B.NUMERO
   AND TO_NUMBER(TO_CHAR(C.FECHA,'YYYY')) = :P_ANO
   AND C.ESTADO <> '9'
   AND A.TP_ART IN ('T','S')
   AND A.COD_ART = B.COD_ART
   AND ((:VENDE = '%') OR (:VENDE <> '%' AND C.COD_VENDE = :VENDE))
   AND X.COD_CLIENTE = C.COD_CLIENTE   
   AND T.TIPO = 29
   AND T.CODIGO = C.COD_VENDE
   AND ((:P_OPC = 'TODOS') OR 
        (:P_OPC = 'TODOS SIN POLIPROPILENO' AND A.COD_ART NOT IN (SELECT COD_ART FROM ARTICUL WHERE TP_ART = 'T' AND COD_FAM = '02' AND COD_LIN IN ('05','0N00'))) OR
		(:P_OPC = 'SOLO POLIPROPILENO' AND A.COD_FAM = '02' AND A.COD_LIN IN ('05','0N00')))
   AND E.UNIDAD = 'KG'
   AND E.COD_ART = A.COD_ART
 GROUP BY C.COD_CLIENTE,
       X.NOMBRE,
       T.ABREVIADA,
       TO_CHAR(C.FECHA,'YYYY/MM'),
	   X.RUC
ORDER BY 2